numeros = [23,234,234,54,3,65,64324,4553,45]

# Encontrando el número mayor de una lista
numero_mas_alto = max(numeros)
print(numero_mas_alto)

# Encontrando el número minimo de una lista
numero_mas_bajo = min(numeros)
print(numero_mas_bajo)

# Redondeando a 6 decimales
numero = round(94.6543216, 6)
print(numero)

# Retorna False -> 0, vacio, False, None \ True -> distinto a 0, Ture, cadena, datos no vacios
resultado_bool = bool(-1)
print(resultado_bool)

# Retorna True, si todos los valores son verdaders
resultado_all = all(["juan","marta","domenica"])
print(resultado_all)

# Suma todos los valores de un iterable
suma_total = sum(numeros)
print(suma_total)