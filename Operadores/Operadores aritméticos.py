# Operadores aritmeticos

# Variables globales
num1 = 89
num2 = 46

# SUMA
resultadoSum = num1 + num2
print("Resultado suma: ", resultadoSum)
# RESTA
resultadoRest = num1 - num2
print("Resultado resta: ", resultadoRest)
# MULTIPLICACIÓN
"""
Si queremos elevar un numero a cualquier potencia pondremos doble asterisco (**) 
"""
resultadoMul = num1 * num2
print("Resultado multiplicación: ", resultadoMul)
resultadoMul = num1 ** num2
print("Resultado de la elevación: ", resultadoMul)
# DIVISIÓN
"""
En Pyhton podemos utilizar el doble slash (//) para poder tener un resultado entero 
"""
resultadoDiv = num1 / num2
print("Resultado división: ", resultadoDiv)
resultadoDiv = num1 // num2
print("Resultado entero de la división: ", resultadoDiv)


