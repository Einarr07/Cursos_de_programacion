# Operadores relacionales

# Varialbes globales
valor1 = 70
valor2 = 59

# Mayor que
resultadoMayor = valor1 > valor2
print(resultadoMayor)

# Menor que
resultadoMenor = valor1 < valor2
print(resultadoMenor)

# Igualdad
resultadoIgualdad = valor1 == valor2
print(resultadoIgualdad)

# Igual o mayor que
resultadoIM = valor1 >= valor2
print(resultadoIM)

# Igual o menor que
resultadoIm = valor1 <= valor2
print(resultadoIm)

# Diferente
resultadoDif = valor1 != valor2
print(resultadoDif)
