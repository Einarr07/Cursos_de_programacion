# Definiendo variables
a = 4
b = 5
c = a + b

print("El valor de a + b es: ", c)

# Concatenación

# Concatenación con +
saludo = "Hola " + "¿Cómo estas?"

print(saludo)

# Concatenación con f-strings
nombre = "Juan"
edad = 20
presentacion = f"Mi nombre es {nombre}, y tengo {edad} años"

print(presentacion)
# Operadores (in / not in)

print("Juan" in presentacion) # True
print("Juan" not in presentacion) # False